class Element
{
private:
	int size, r, c;
public:
	int getSize()
	{
		return size;
	}
	int getR()
	{
		return r;
	}
	int getC()
	{
		return c;
	}
	void setSize(int size)
	{
		this->size = size;
	}
	void setR(int r)
	{
		this->r = r;
	}
	void setC(int c)
	{
		this->c = c;
	}
};